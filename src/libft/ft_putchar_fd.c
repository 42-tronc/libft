#include "../../header/libft.h"

/**
 * @brief writes char 'c' to file descriptor 'fd'
 *
 * @param c char to write
 * @param fd file descriptor in which to write
 */
void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
