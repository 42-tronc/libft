#include "../../header/libft.h"

/**
 * @brief writes string 's' followed by a new line to 'fd'
 *
 * @param s string to write
 * @param fd file descriptor to write to
 */
void	ft_putendl_fd(char *s, int fd)
{
	if (!s)
		return ;
	ft_putstr_fd(s, fd);
	write(fd, "\n", 1);
}
