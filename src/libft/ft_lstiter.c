#include "../../header/libft.h"

/**
 * @brief iterates the list 'lst' and
 * applies the function 'f' on the content of each node
 *
 * @param lst address of a pointer to the node
 * @param f address of the function to iterate on 'lst'
 */
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		f(lst->content);
		lst = lst->next;
	}
}
